public class Personnage {

    public Orientation tourner(int nbre){
        if(nbre = 1){
            return Orientation.EST;
        }

        if (nbre = 1){
            return Orientation.SUD;
        }
        if (nbre = 1){
            return Orientation.EST;
        }
        return Orientation.NORD;


    }
}
